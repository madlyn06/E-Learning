#### Cách sử dụng các chức năng liên quan tới story

********************************

Ở trang chính thì có thể nhập tên của story để tìm kiếm thông tin.

<img src="{{ asset('vendor/cms/assets/admin/images/st1.png') }}" width="100%" height="530" alt="SEO"/>

********************************

1. Tạo mới, hiển thị ra ngoài giao diện

Để thêm khách hàng mới, chọn ``` Thêm ``` bên góc trên phải màn hình. Ở màn hình này, nếu không chọn hình ảnh nào thì mặc định
sẽ lấy 1 hình ảnh bất kỳ trong stories để làm hình hiển thị đầu tiên. Đối với nhạc nền ở đây sẽ làm nhạc cho toàn bộ stories. Sau đó nhập thông tin và submit.

********************************

<img src="{{ asset('vendor/cms/assets/admin/images/st2.png') }}" width="100%" height="530" alt="SEO"/>

********************************

Sau khi submit thông tin tạo mới story thì sẽ hiển thị ra trang như sau:

<img src="{{ asset('vendor/cms/assets/admin/images/st5.png') }}" width="100%" height="530" alt="SEO"/>

********************************

Trang này sẽ hiển thị chi tiết về story vừa được tạo.

********************************

2. Sửa story và thêm các story item

Khi vào trang edit story cha thì sẽ hiển thị ra 2 block.
- Thứ 1 bên trái là các stories con thuộc về story cha (nếu có).
- Thứ 2 bên phải là form để sửa thông tin trong story cha.

Nếu muốn tạo mới story con thì chỉ cần click vào button `Thêm` ở block bên trái góc phải trên.

<img src="{{ asset('vendor/cms/assets/admin/images/st3.png') }}" width="100%" height="530" alt="SEO"/>

********************************

3. Tạo story con
 
* Lưu ý:
  - Những story nào có `Kích hoạt` thì mới hiển thị ra ngoài.
  - Link: chỉ nhập uri, không nhập toàn bộ link (full link).
  - Nếu link bỏ trống hoặc nhập sai uri thì bài viết sẽ trỏ tới bài viết nếu tồn tại hoặc trang chủ.
  - Những input có sao (*) đỏ là phần bắt buộc phải nhập.

<img src="{{ asset('vendor/cms/assets/admin/images/st4.png') }}" width="100%" height="530" alt="SEO"/>

********************************

** Lưu ý về hình ảnh:

Để có hình ảnh đẹp nhất, chúng tôi khuyến khích thông tin về hình ảnh như sau:

> - Width: 285px
>
> - Height: 427px
>
> - Tên: độ dài  <= 8 chữ
>
> - Mô tả story item: độ dài <= 18 chữ
>

