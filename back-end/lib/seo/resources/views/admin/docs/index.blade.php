#### Internal links

Ở trang quản lý chính của internal links sẽ hiển thị toàn bộ từ khoá và giá trị của từ khoá đó.

Tạo mới internal link gồm `Từ khoá` cần gắn link, `Giá trị` là link cần gắn khi tạo mới hoặc update bài viết.

