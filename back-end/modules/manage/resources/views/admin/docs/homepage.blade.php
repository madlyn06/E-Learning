Vào chi tiết của từng item sẽ có các thông tin như sau

<img src="{{ asset('vendor/manage/assets/docs/admin/images/homepage4.png') }}" width="100%" height="530" alt="Service"/>

Các thông tin này sẽ được hiển thị tương ứng ở ngoài trang chủ như sau

<img src="{{ asset('vendor/manage/assets/docs/admin/images/homepage5.png') }}" width="100%" height="530" alt="Service"/>


Để thêm một slider item mới, chọn ``` Thêm ``` bên góc trên phải màn hình ở block thứ nhất. Sau đó nhập thông tin và submit
như ở màn hình edit.
********************************

Nếu dịch vụ đó có trạng thái là `Kích hoạt` thì sẽ được random và hiển thị ra ngoài màn hình chính.
* Lưu ý: các trường thông tin có dấu * đỏ là các trường bắt buộc phải nhập.
********************************

Tương tự, để sửa thông tin đã tạo thì chỉ cần ra ngoài màn hình giao diện chính của trang Dịch vụ và click vào biểu tượng sửa bên cạnh biểu tượng xoá.
Sau đó ra ngoài giao diện reload lại (F5) thì sẽ thấy sự thay đổi.

