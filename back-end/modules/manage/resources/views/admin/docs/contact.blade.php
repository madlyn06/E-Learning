##### Các phần chính
1. Giao diện chính

<img src="{{ asset('vendor/manage/assets/docs/admin/images/contact1.png') }}" width="100%" height="530" alt="contact"/>

********************************
Ở trang chính thì có thể nhập tên hoặc email để tìm kiếm thông tin.

2. Trả lời thông tin liên hệ

Để trả lời câu hỏi trong thông tin liên hệ thì chỉ cần click vào biểu tượng trả lời bên cạnh biểu tượng xoá ở trang chủ,
lúc này một popup được mở lên và bạn có thể trả lời thông tin. Sau khi click vào `Reply Now` lúc này nội dung trả lời sẽ
được gởi đến email của người liên hệ.

*** Chú ý: phần nội dung không được bỏ trống.

<img src="{{ asset('vendor/manage/assets/docs/admin/images/contact2.png') }}" width="100%" height="530" alt="contact"/>

********************************

Bên cạnh đó, khi có một thông tin liên hệ mới được gởi đi từ giao diện người dùng thì sẽ có một email được đến trong hòm thử được cài đặt trong
phần [cài đặt hệ thống](/admin/setting), bạn có thể chỉnh sửa email cần gởi đến tại đây.

